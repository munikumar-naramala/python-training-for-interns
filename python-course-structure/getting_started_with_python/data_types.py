def int_example():
    x = 5
    print(x)
    print(type(x))
    print("----------")


def float_example():
    x = 5.3
    print(x)
    print(type(x))
    print("----------")


def string_example():
    x = "Sanjana"
    print(x)
    print(type(x))
    print("----------")


def complex_example():
    x = 21 + 3j
    print(x)
    print(type(x))
    print("----------")


def boolean_example():
    x = True
    print(x)
    print(type(x))
    print("----------")


if __name__ == '__main__':
    int_example()
    float_example()
    string_example()
    complex_example()
    boolean_example()
