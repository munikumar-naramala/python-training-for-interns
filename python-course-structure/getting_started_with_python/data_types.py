def data_types():
    def int_example():
        x = 5
        print(x)
        print(type(x))
        print("----------")

    int_example()

    def float_example():
        x = 5.3
        print(x)
        print(type(x))
        print("----------")

    float_example()

    def string_example():
        x = "Sanjana"
        print(x)
        print(type(x))
        print("----------")

    string_example()

    def complex_example():
        x = 21 + 3j
        print(x)
        print(type(x))
        print("----------")

    complex_example()

    def boolean_example():
        x = True
        print(x)
        print(type(x))
        print("----------")

    boolean_example()


if __name__ == '__main__':
    data_types()
